public class Coin
{
    // Fields
    private double value;
    private String name;
    
    // Constructors
    public Coin(double aValue, String aName) {
        value = aValue;
        name = aName;
    }
    
    // Methods
    public String getName() {
        return name;
    }
    
    public double getValue() {
        return value;
    }
}
